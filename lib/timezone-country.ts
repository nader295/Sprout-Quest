export function detectCountryFromTimezone(): { code: string; name: string } | null {
  try {
    const tz = Intl.DateTimeFormat().resolvedOptions().timeZone;
    if (!tz) return null;

    // Direct match
    if (TIMEZONE_COUNTRY[tz]) return TIMEZONE_COUNTRY[tz];

    // Fuzzy match — e.g. "Europe/London" prefix
    const prefix = tz.split("/")[0];
    const fallback = Object.entries(TIMEZONE_COUNTRY).find(([k]) => k.startsWith(prefix + "/"));
    return fallback ? fallback[1] : null;
  } catch {
    return null;
  }
}
